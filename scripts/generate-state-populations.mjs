#!/usr/bin/env node
/**
 * Fetches Census state (and DC) population estimates and emits state-populations.ts
 * with POP: Record<string, number> (state abbrev -> population) for both server functions.
 * Uses the same Census co-est2023 county totals CSV as generate-county-populations.mjs;
 * state totals = sum of county populations per state (same vintage, no API key).
 * Re-run to refresh state/national totals. Run: node scripts/generate-state-populations.mjs
 */

import { createWriteStream } from "fs";
import { mkdir } from "fs/promises";
import { dirname, join } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const OUT_DIRS = [
  join(__dirname, "../supabase/functions/make-server-283d8046"),
  join(__dirname, "../supabase/functions/server"),
];

const CSV_URL =
  "https://www2.census.gov/programs-surveys/popest/datasets/2020-2023/counties/totals/co-est2023-alldata.csv";

// Census state FIPS (50 states + DC). 11 = DC, 24 = MD. Matches county CSV STATE column.
const FIPS_TO_ABBREV = {
  "01": "AL", "02": "AK", "04": "AZ", "05": "AR", "06": "CA", "08": "CO",
  "09": "CT", "10": "DE", "11": "DC", "12": "FL", "13": "GA", "15": "HI",
  "16": "ID", "17": "IL", "18": "IN", "19": "IA", "20": "KS", "21": "KY",
  "22": "LA", "23": "ME", "24": "MD", "25": "MA", "26": "MI", "27": "MN",
  "28": "MS", "29": "MO", "30": "MT", "31": "NE", "32": "NV", "33": "NH",
  "34": "NJ", "35": "NM", "36": "NY", "37": "NC", "38": "ND", "39": "OH",
  "40": "OK", "41": "OR", "42": "PA", "44": "RI", "45": "SC", "46": "SD",
  "47": "TN", "48": "TX", "49": "UT", "50": "VT", "51": "VA", "53": "WA",
  "54": "WV", "55": "WI", "56": "WY",
};

async function fetchStatePopulations() {
  const res = await fetch(CSV_URL);
  if (!res.ok) throw new Error(`Census CSV fetch failed: ${res.status}`);
  const text = await res.text();
  const lines = text.split(/\r?\n/);
  const header = lines[0].split(",");
  const idx = (name) => {
    const i = header.indexOf(name);
    if (i === -1) throw new Error(`Column not found: ${name}`);
    return i;
  };
  const iSumLev = idx("SUMLEV");
  const iState = idx("STATE");
  const iCounty = idx("COUNTY");
  const iPop = idx("POPESTIMATE2023");
  const out = {};
  for (let i = 1; i < lines.length; i++) {
    const row = lines[i].split(",");
    if (row.length < Math.max(iSumLev, iState, iCounty, iPop) + 1) continue;
    if (row[iSumLev] !== "050") continue; // county level only
    const stateFips = String(row[iState]).padStart(2, "0");
    const abbrev = FIPS_TO_ABBREV[stateFips];
    if (!abbrev) continue;
    const pop = parseInt(row[iPop], 10);
    if (Number.isNaN(pop)) continue;
    out[abbrev] = (out[abbrev] || 0) + pop;
  }
  return out;
}

const pop = await fetchStatePopulations();

const sorted = Object.entries(pop).sort(([a], [b]) => a.localeCompare(b));
const lines = [
  "// State (and DC) population estimates (Census POPESTIMATE2023, summed from county totals).",
  "// Generated by scripts/generate-state-populations.mjs — re-run to refresh.",
  "",
  "export const POP: Record<string, number> = {",
  ...sorted.map(([abbrev, n]) => `  ${abbrev}: ${n},`),
  "};",
  "",
];
const content = lines.join("\n");

for (const dir of OUT_DIRS) {
  await mkdir(dir, { recursive: true });
  const outPath = join(dir, "state-populations.ts");
  const stream = createWriteStream(outPath, "utf8");
  stream.write(content);
  stream.end();
  await new Promise((r) => stream.on("finish", r));
  console.log(`Wrote ${outPath} with ${sorted.length} states.`);
}

const total = sorted.reduce((s, [, n]) => s + n, 0);
console.log(`National total (sum of states + DC): ${(total / 1e6).toFixed(2)} million`);
